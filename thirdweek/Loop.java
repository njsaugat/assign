package com.assign.thirdweek;

public class Loop {
	public static void main(String[] args) {
		//for loop
		for(int i=0;i<=5;i++) {
			System.out.println(i);
		}
		System.out.println(" ");
		//while loop 
		int j=5;
		while(j>0) {
			System.out.println(j);
			j--;
		}
		System.out.println(" ");
		//do while loop
		int k=0;
		do {
			System.out.println(k);
			k++;
		}while(k<=5);
	}}
